    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        String nama= txtpasien.getText();
        String tl = txttl.getText();
        String usia = txtusia.getText();
        String jk = null;
        if(rlaki.isSelected()){
            jk=rlaki.getText();
        }
        else if (rper.isSelected()){
            jk=rper.getText();
        }
        String diagnosa =(String) listdia.getSelectedValue();
        String dokter = cbdokter.getSelectedItem().toString();
        String ruangan = cbruangan.getSelectedItem().toString();
        String tgl_masuk = txtmasuk.getText();
        TambahData(nama,tl,usia,jk,diagnosa,dokter,ruangan,tgl_masuk);
        InitTable();
        TampilData();
        
        txtpasien.requestFocus();
         btnreset.setEnabled(true);
    }//GEN-LAST:event_btnsimpanActionPerformed

