    void filterhuruf(KeyEvent a){
        if(Character.isAlphabetic(a.getKeyChar())){
            a.consume();
            JOptionPane.showMessageDialog(null,"Nomor Telpon Hanya Bisa Karakter Angka","PERINGATAN!",JOptionPane.WARNING_MESSAGE);
        }
    }
void filterAngka(KeyEvent b){
if(Character.isDigit(b.getKeyChar())){
            b.consume();
            JOptionPane.showMessageDialog(null,"Nama Hanya Bisa Karakter Huruf","PERINGATAN!",JOptionPane.WARNING_MESSAGE);
        }    
}
