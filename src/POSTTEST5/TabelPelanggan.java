    private void txtnamaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtnamaKeyPressed
        if(evt.getKeyCode()== evt.VK_ENTER){ //memindahkan kursor kebawah
            {
            if(txtnama.getText().length()==0)
            {
                JOptionPane.showMessageDialog(null,"Nama Tidak Boleh Kosong","PERINGATAN", JOptionPane.INFORMATION_MESSAGE);
                txtnama.requestFocus();
                return;
            }
        }   
            txtnim.requestFocus();
            }
    }//GEN-LAST:event_txtnamaKeyPressed

