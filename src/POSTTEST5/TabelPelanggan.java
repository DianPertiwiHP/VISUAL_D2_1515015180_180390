    private void btnupdateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnupdateMouseClicked
        btnsimpan.setEnabled(false);
        btntambah.setEnabled(false);
        try{
            int baris = jTable1.getSelectedRow();

            jTable1.setValueAt(txtnama.getText(), baris, 0);
            jTable1.setValueAt(txtnim.getText(), baris, 1);
            jTable1.setValueAt(txttelp.getText(), baris, 2);
            jTable1.setValueAt(cbasal.getSelectedItem(), baris, 3);
        }
        catch(ArrayIndexOutOfBoundsException e){
            JOptionPane.showMessageDialog(this,"Pilih Table! ","PERINGATAN!",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnupdateMouseClicked

